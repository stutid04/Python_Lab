num1 = float(input("Enter the first floating point number: "))
num2 = float(input("Enter the second floating point number: "))
addition = num1 + num2
subtraction = num1 - num2
multiplication = num1 * num2
division = num1 / num2
print("Addition: {:.2f}".format(addition))
print("Subtraction: {:.2f}".format(subtraction))
print("Multiplication: {:.2f}".format(multiplication))
print("Division: {:.2f}".format(division))
